    private int vtMaDV(int ma, List<DichVu> l) {
        for(int i = 0; i < l.size(); i++)
            if(l.get(i).getMa() == ma)
                return i;
        return 0;
    }
    
    private int vtMaNV(int ma, List<NhanVien> l) {
        for(int i = 0; i < l.size(); i++)
            if(l.get(i).getMa() == ma)
                return i;
        return 0;
    }

