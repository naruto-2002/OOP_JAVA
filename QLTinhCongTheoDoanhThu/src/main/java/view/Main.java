    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        if(ldv.size() > 0)
            DichVu.setSma(ldv.get(ldv.size() - 1).getMa() + 1);
        jTextField1.setText(DichVu.getSma()+"");
        jTextField2.requestFocus();
        
        int ma;
        String ten, nhom;
        int CP, GC;
        
        try {
            ma = Integer.parseInt(jTextField1.getText());
            ten = jTextField2.getText();
            nhom = jComboBox1.getSelectedItem().toString();
            if(ten.isEmpty() || nhom.isEmpty()) throw new TrongException();
            CP = Integer.parseInt(jTextField3.getText());
            GC = Integer.parseInt(jTextField4.getText());
            if(CP < 0 || GC < 0) throw new SoDuongException();
            DichVu dv = new DichVu(ma, ten, nhom, CP, GC);
            tm1.addRow(dv.toObjects());
            ldv.add(dv);
            jTextField1.setText(Integer.toString(ma + 1));
            jTextField2.setText("");
            jTextField3.setText("");
            jComboBox1.setSelectedIndex(0);
            jTextField4.setText("");
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "khong de trong!!!");
            jTextField2.requestFocus();
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap so");
            jTextField4.requestFocus();
        } catch(SoDuongException e) {
            JOptionPane.showMessageDialog(this, "Nhap so duong");
            jTextField4.requestFocus();
        }
        
        
    }//GEN-LAST:event_btThemActionPerformed

