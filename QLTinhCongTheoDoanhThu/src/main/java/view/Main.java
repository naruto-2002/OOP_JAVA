    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        int row = jTable2.getSelectedRow();
        if(row < 0 || row >= jTable2.getRowCount()) {
            JOptionPane.showMessageDialog(this, "Chon dong de sua");
        }else {
            int ma;
            String hoten, DC;
            int soDT;

            try {
                ma = Integer.parseInt(jTextField5.getText());
                hoten = jTextField6.getText();
                DC = jTextField7.getText();
                if(hoten.isEmpty() || DC.isEmpty()) throw new TrongException();
                String pattern = "\\d+";
                if(!jTextField8.getText().matches(pattern)) throw new SoDTException();
                soDT = Integer.parseInt(jTextField8.getText());
                NhanVien nv = new NhanVien(ma, hoten, DC, soDT);
                tm2.removeRow(row);
                tm2.insertRow(row, nv.toObjects());
                lnv.set(row, nv);
            } catch (TrongException e) {
                JOptionPane.showMessageDialog(this, "khong de trong!!!");
                jTextField6.requestFocus();
            } catch(SoDTException e) {
                JOptionPane.showMessageDialog(this, "Nhap dung so dien thoai");
                jTextField8.requestFocus();
            }
        }
    }//GEN-LAST:event_btSuaActionPerformed

